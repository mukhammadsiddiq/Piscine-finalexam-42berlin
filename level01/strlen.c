#include <stdio.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while(str[i])
		i++;
	return (i);
}

int	main(void)
{
	char	*s = "Mukhammad";
	printf("%d lenght", ft_strlen(s));
}